// select the root node
const root = document.querySelector("body");
console.log(root);
// select the last child of the root node
const lec = document.querySelector("body").lastElementChild;
console.log(lec);
// select all the children of the body element
const cbe = document.querySelector("body").children;
console.log(cbe);
// select the next sibling of the h2 node
const nsh2 = document.querySelector("h2").nextElementSibling;
console.log(nsh2);
// select the parent element of the h1 node
const peh1 = document.querySelector("h1").parentElement;
console.log(peh1);
